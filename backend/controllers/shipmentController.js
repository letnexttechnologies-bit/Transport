import Shipment from '../models/Shipment.js';
import Booking from '../models/Booking.js';

// @desc    Get all shipments
// @route   GET /api/shipments
// @access  Public
export const getShipments = async (req, res) => {
  try {
    const { status, createdBy } = req.query;
    const query = {};

    if (status) {
      query.status = status;
    }
    if (createdBy) {
      query.createdBy = createdBy;
    }

    const shipments = await Shipment.find(query)
      .populate('createdBy', 'name phone')
      .sort({ createdAt: -1 });

    // Ensure shipments have shipmentId (for existing shipments without it)
    // Only update if missing to avoid unnecessary writes
    const shipmentsNeedingId = shipments.filter(s => !s.shipmentId);
    if (shipmentsNeedingId.length > 0) {
      const existingCount = await Shipment.countDocuments({ shipmentId: { $exists: true } });
      for (let i = 0; i < shipmentsNeedingId.length; i++) {
        shipmentsNeedingId[i].shipmentId = `SH${String(existingCount + i + 1).padStart(2, '0')}`;
        await shipmentsNeedingId[i].save();
      }
      // Re-fetch to get updated shipmentIds
      const updatedShipments = await Shipment.find(query)
        .populate('createdBy', 'name phone')
        .sort({ createdAt: -1 });
      shipments.splice(0, shipments.length, ...updatedShipments);
    }

    // Get booking status for each shipment
    const shipmentIds = shipments.map(s => s._id);
    const activeBookings = await Booking.find({
      shipmentId: { $in: shipmentIds },
      status: { $in: ['Pending', 'Approved'] },
    }).populate('userId', 'name');

    // Create a map of shipmentId -> booking
    const bookingMap = {};
    activeBookings.forEach(booking => {
      bookingMap[booking.shipmentId.toString()] = {
        isBooked: true,
        bookedBy: booking.userName,
        bookingStatus: booking.status,
        bookingId: booking._id,
      };
    });

    // Add booking status to each shipment
    const shipmentsWithBookingStatus = shipments.map(shipment => {
      const bookingInfo = bookingMap[shipment._id.toString()];
      return {
        ...shipment.toObject(),
        isBooked: bookingInfo ? true : false,
        bookedBy: bookingInfo?.bookedBy || null,
        bookingStatus: bookingInfo?.bookingStatus || null,
      };
    });

    res.json(shipmentsWithBookingStatus);
  } catch (error) {
    res.status(500).json({ message: error.message });
  }
};

